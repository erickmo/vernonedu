import '../../domain/entities/invoice_detail_entity.dart';

class PaymentHistoryEntryModel extends PaymentHistoryEntry {
  const PaymentHistoryEntryModel({
    required super.id,
    required super.paidAt,
    required super.amount,
    required super.method,
    super.proofUrl,
    required super.recordedBy,
  });

  factory PaymentHistoryEntryModel.fromJson(Map<String, dynamic> json) =>
      PaymentHistoryEntryModel(
        id: json['id'] as String? ?? '',
        paidAt: DateTime.tryParse(json['paid_at'] as String? ?? '') ??
            DateTime.now(),
        amount: (json['amount'] as num?)?.toDouble() ?? 0,
        method: json['method'] as String? ?? '',
        proofUrl: json['proof_url'] as String?,
        recordedBy: json['recorded_by'] as String? ?? '',
      );
}

class InvoiceDetailModel extends InvoiceDetailEntity {
  const InvoiceDetailModel({
    required super.id,
    required super.invoiceNumber,
    required super.studentName,
    required super.studentContact,
    required super.batchCode,
    required super.batchName,
    required super.courseTypeName,
    required super.paymentMethod,
    required super.amount,
    required super.createdAt,
    required super.dueDate,
    required super.status,
    required super.source,
    super.notes,
    super.cancelReason,
    required super.paymentHistory,
  });

  factory InvoiceDetailModel.fromJson(Map<String, dynamic> json) {
    final historyRaw = json['payment_history'] as List<dynamic>? ?? [];
    final history = historyRaw
        .map((e) =>
            PaymentHistoryEntryModel.fromJson(e as Map<String, dynamic>))
        .toList();

    return InvoiceDetailModel(
      id: json['id'] as String? ?? '',
      invoiceNumber: json['invoice_number'] as String? ?? '',
      studentName: json['student_name'] as String? ?? '',
      studentContact: json['student_contact'] as String? ?? '',
      batchCode: json['batch_code'] as String? ?? '',
      batchName: json['batch_name'] as String? ?? '',
      courseTypeName: json['course_type_name'] as String? ?? '',
      paymentMethod: json['payment_method'] as String? ?? '',
      amount: (json['amount'] as num?)?.toDouble() ?? 0,
      createdAt:
          DateTime.tryParse(json['created_at'] as String? ?? '') ?? DateTime.now(),
      dueDate:
          DateTime.tryParse(json['due_date'] as String? ?? '') ?? DateTime.now(),
      status: json['status'] as String? ?? 'draft',
      source: json['source'] as String? ?? 'auto',
      notes: json['notes'] as String?,
      cancelReason: json['cancel_reason'] as String?,
      paymentHistory: history,
    );
  }

  InvoiceDetailEntity toEntity() => InvoiceDetailEntity(
        id: id,
        invoiceNumber: invoiceNumber,
        studentName: studentName,
        studentContact: studentContact,
        batchCode: batchCode,
        batchName: batchName,
        courseTypeName: courseTypeName,
        paymentMethod: paymentMethod,
        amount: amount,
        createdAt: createdAt,
        dueDate: dueDate,
        status: status,
        source: source,
        notes: notes,
        cancelReason: cancelReason,
        paymentHistory: paymentHistory,
      );

